// Page navigation

document.querySelector('.nav__links').addEventListener('click', function (e) {
  e.preventDefault();

  // Matching strategy
  if (e.target.classList.contains('nav__link')) {
    const id = e.target.getAttribute('href');
    document.querySelector(id).scrollIntoView({ behavior: 'smooth' });
  }
});

///////////////////////////////////////
// Tabbed component

tabContainer.addEventListener('click', function (e) {
  const clicked = e.target.closest('.benefits__tab');

  // Guard clause
  if (!clicked) return;

  // Remove active classes
  tabs.forEach(t => t.classList.remove('benefits__tab--active'));
  tabContent.forEach(c => c.classList.remove('benefits__content--active'));

  // Activate tab
  clicked.classList.add('benefits__tab--active');

  // Activate content area
  document
    .querySelector(`.benefits__content--${clicked.dataset.tab}`)
    .classList.add('benefits__content--active');
});

